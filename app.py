def av_color(name: str) -> str:
    return AVATAR_COLORS[int(hashlib.md5(name.encode()).hexdigest(), 16) % len(AVATAR_COLORS)]

def av_initials(name: str) -> str:
    s = name.upper()
    for p in ["MR. ","MS. ","MRS. ","DR. "]:
        s = s.replace(p, "")
    parts = s.strip().split()
    return (parts[0][0] + (parts[1][0] if len(parts) > 1 else "")).upper()

def short_name(name: str) -> str:
    for p in ["MR. ","MS. ","MRS. ","DR. "]:
        if name.upper().startswith(p):
            return name[len(p):].strip()
    return name.strip()

@st.cache_data
def load_master() -> pd.DataFrame:
    df = pd.read_csv(MASTER_CSV)
    df["Teacher_Name"] = df["Teacher_Name"].str.strip()
    df["Class"]        = df["Class"].fillna("").str.strip()
    df["Subject"]      = df["Subject"].fillna("").str.strip()
    return df

def get_schedule(df, teacher, day):
    return df[(df["Teacher_Name"]==teacher) & (df["Day"]==day)].sort_values("Period").reset_index(drop=True)

def busy_set(df, day, period) -> set:
    return set(df[(df["Day"]==day) & (df["Period"]==period)]["Teacher_Name"])

def teacher_period_info(df, teacher, day, period):
    rows = df[(df["Teacher_Name"]==teacher) & (df["Day"]==day) & (df["Period"]==period)]
    return (rows.iloc[0]["Class"], rows.iloc[0]["Subject"]) if not rows.empty else ("","")

def master_load(df, teacher, day) -> int:
    return len(df[(df["Teacher_Name"]==teacher) & (df["Day"]==day)])

def append_log(record: dict) -> None:
    log = pd.DataFrame([record])
    if os.path.exists(LOG_CSV):
        log.to_csv(LOG_CSV, mode="a", header=False, index=False)
    else:
        log.to_csv(LOG_CSV, index=False)

def whatsapp_text(report_df: pd.DataFrame, day: str, date_val) -> str:
    lines = [f"📋 *KV BURHANPUR ARRANGEMENT*", f"📅 {day}, {date_val}", ""]
    absent_list = report_df["Absent_Teacher"].unique()
    lines.append(f"🔴 Absent: {', '.join(short_name(t) for t in absent_list)}")
    lines.append("")
    for period in sorted(report_df["Period"].unique()):
        lines.append(f"*Period {period}*")
        for _, r in report_df[report_df["Period"]==period].iterrows():
            if r["Type"] == "CLUBBED":
                club_info = r.get("Sub_Own_Class","")
                if r.get("Sub_Own_Subject"): club_info += f" · {r['Sub_Own_Subject']}"
                club_note = f" _(clubbing with {club_info})_" if club_info else ""
                lines.append(
                    f"  🔀  {r['Class']} ({r['Subject']}) — "
                    f"{short_name(r['Absent_Teacher'])} → *{short_name(r['Substitute'])}*{club_note}"
                )
            else:
                lines.append(
                    f"  ✅  {r['Class']} ({r['Subject']}) — "
                    f"{short_name(r['Absent_Teacher'])} → *{short_name(r['Substitute'])}*"
                )
        lines.append("")
    lines.append("_Generated by KV Burhanpur Arrangement App_")
    return "\n".join(lines)


