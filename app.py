def generate_arrangement_pdf(report_df: pd.DataFrame, selected_day: str, date_val) -> bytes:
    import io
    from reportlab.lib import colors as C
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import ParagraphStyle
    from reportlab.lib.units import cm
    from reportlab.lib.enums import TA_CENTER, TA_LEFT
    from reportlab.platypus import (
        SimpleDocTemplate, Table, TableStyle,
        Paragraph, Spacer, HRFlowable,
    )

    # ── palette ──────────────────────────────────────────────────────────────
    BLUE   = C.HexColor("#1E40AF")
    BLUE2  = C.HexColor("#DBEAFE")
    STRIPE = C.HexColor("#F8FAFC")
    BORDER = C.HexColor("#CBD5E1")
    RED    = C.HexColor("#DC2626")
    ORANGE = C.HexColor("#D97706")
    DARK   = C.HexColor("#1E293B")
    MID    = C.HexColor("#64748B")
    GREEN  = C.HexColor("#15803D")

    # ── paragraph styles ─────────────────────────────────────────────────────
    def ps(name, font="Helvetica", size=8, align=TA_CENTER, textColor=DARK, **kw):
        return ParagraphStyle(name, fontName=font, fontSize=size,
                              alignment=align, textColor=textColor, **kw)

    sty_school  = ps("school", "Helvetica-Bold", 13, textColor=BLUE, spaceAfter=2)
    sty_title   = ps("title",  "Helvetica-Bold", 10, textColor=DARK, spaceAfter=1)
    sty_meta    = ps("meta",   size=8, textColor=MID, spaceAfter=0)
    sty_hdr     = ps("hdr",    "Helvetica-Bold", 8, textColor=C.white)
    sty_cell_c  = ps("cc",     size=8)
    sty_cell_l  = ps("cl",     size=8, align=TA_LEFT)
    sty_bold_l  = ps("bl",     "Helvetica-Bold", 8, align=TA_LEFT, textColor=DARK)
    sty_unassign= ps("ua",     "Helvetica-Bold", 8, align=TA_LEFT, textColor=RED)
    sty_clubbed = ps("cb",     "Helvetica-Bold", 8, align=TA_LEFT, textColor=ORANGE)
    sty_sig_hdr = ps("sh",     "Helvetica-Bold", 9, textColor=DARK)
    sty_sig_sub = ps("ss",     size=7.5, textColor=MID, spaceBefore=2)

    # ── document ─────────────────────────────────────────────────────────────
    buf = io.BytesIO()
    doc = SimpleDocTemplate(
        buf, pagesize=A4,
        leftMargin=1.5*cm, rightMargin=1.5*cm,
        topMargin=1.5*cm, bottomMargin=2*cm,
        title=f"Arrangement {date_val}",
    )

    elems = []

    # header
    elems.append(Paragraph("PM SHRI KENDRIYA VIDYALAYA BURHANPUR", sty_school))
    elems.append(Paragraph("DAILY TEACHER ARRANGEMENT", sty_title))
    elems.append(Paragraph(f"{selected_day}  ·  {date_val}  ·  Academic Year 2026-27", sty_meta))
    elems.append(Spacer(1, 0.2*cm))
    elems.append(HRFlowable(width="100%", thickness=1.5, color=BLUE, spaceAfter=0.25*cm))
    elems.append(Spacer(1, 0.15*cm))

    # ── table ────────────────────────────────────────────────────────────────
    # Sort: absent teacher → period
    sdf = report_df.sort_values(["Absent_Teacher", "Period"]).reset_index(drop=True)

    col_w = [0.9*cm, 3.7*cm, 0.9*cm, 2.1*cm, 2.1*cm, 3.7*cm, 1.4*cm, 2.2*cm]
    # total = 17cm  (A4 portrait usable ≈ 18cm)

    col_labels = ["S.No", "Absent Teacher", "Per.", "Class /\nSection",
                  "Subject", "Substitute Teacher", "Mode", "Sign"]
    tbl_data = [[Paragraph(h, sty_hdr) for h in col_labels]]

    for idx, row in sdf.iterrows():
        absent  = short_name(row["Absent_Teacher"])
        sub     = row.get("Substitute", "— Not Assigned —")
        mode    = row.get("Type", "SUBSTITUTE")
        own_cls = row.get("Sub_Own_Class", "")

        if sub == "— Not Assigned —":
            sub_p  = Paragraph("UNASSIGNED ⚠", sty_unassign)
            mode_p = Paragraph("—",            sty_cell_c)
        elif mode == "CLUBBED":
            own_subj = row.get("Sub_Own_Subject", "")
            club_detail = own_cls + (f" · {own_subj}" if own_subj else "")
            sub_text = short_name(sub) + (f"\n(clubbing: {club_detail})" if club_detail else "")
            sub_p  = Paragraph(sub_text, sty_clubbed)
            mode_p = Paragraph("CLUB",   ps("cn","Helvetica-Bold",6.5,textColor=ORANGE))
        else:
            sub_p  = Paragraph(short_name(sub), sty_bold_l)
            mode_p = Paragraph("SUB",           ps("sm",size=7.5,textColor=GREEN))

        tbl_data.append([
            Paragraph(str(idx + 1),                sty_cell_c),
            Paragraph(absent,                      sty_cell_l),
            Paragraph(str(row["Period"]),           sty_cell_c),
            Paragraph(str(row.get("Class", "")),    sty_cell_c),
            Paragraph(str(row.get("Subject", "")),  sty_cell_c),
            sub_p, mode_p,
            Paragraph("", sty_cell_c),   # Sign column (blank)
        ])

    tbl = Table(tbl_data, colWidths=col_w, repeatRows=1)

    ts = TableStyle([
        ("BACKGROUND",   (0,0), (-1,0),  BLUE),
        ("ALIGN",        (0,0), (-1,0),  "CENTER"),
        ("VALIGN",       (0,0), (-1,-1), "MIDDLE"),
        ("TOPPADDING",   (0,0), (-1,-1), 5),
        ("BOTTOMPADDING",(0,0), (-1,-1), 5),
        ("LEFTPADDING",  (0,0), (-1,-1), 4),
        ("RIGHTPADDING", (0,0), (-1,-1), 4),
        ("GRID",         (0,0), (-1,-1), 0.4, BORDER),
        ("LINEBELOW",    (0,0), (-1,0),  1.5, BLUE),
    ])

    # Zebra stripes
    for i in range(1, len(tbl_data)):
        ts.add("BACKGROUND", (0,i), (-1,i), STRIPE if i % 2 == 0 else C.white)

    # Highlight unassigned rows
    for i, (_, row) in enumerate(sdf.iterrows(), start=1):
        if row.get("Substitute", "") == "— Not Assigned —":
            ts.add("BACKGROUND", (0,i), (-1,i), C.HexColor("#FEF2F2"))

    # Merge absent-teacher cells for consecutive same teacher
    prev_t, grp_s = None, None
    groups: list[tuple[int,int]] = []
    for i, (_, row) in enumerate(sdf.iterrows()):
        ridx = i + 1
        t = row["Absent_Teacher"]
        if t != prev_t:
            if grp_s is not None and ridx - 1 > grp_s:
                groups.append((grp_s, ridx - 1))
            grp_s, prev_t = ridx, t
    if grp_s is not None and len(sdf) > grp_s:
        groups.append((grp_s, len(sdf)))

    for r0, r1 in groups:
        ts.add("SPAN",   (1, r0), (1, r1))
        ts.add("VALIGN", (1, r0), (1, r1), "MIDDLE")
        ts.add("LINEBELOW", (1, r0), (1, r1-1), 0, C.white)  # hide inner borders

    tbl.setStyle(ts)
    elems.append(tbl)

    # ── footer ────────────────────────────────────────────────────────────────
    elems.append(Spacer(1, 0.9*cm))
    elems.append(HRFlowable(width="100%", thickness=0.5, color=BORDER, spaceAfter=0.5*cm))

    sig_data = [
        [Paragraph("", sty_sig_sub),              "",
         Paragraph("", sty_sig_sub)],
        [Paragraph("_" * 35, sty_sig_sub),        "",
         Paragraph("_" * 35, sty_sig_sub)],
        [Paragraph("Time-Table In-Charge", sty_sig_hdr), "",
         Paragraph("PRINCIPAL", sty_sig_hdr)],
        [Paragraph("PM Shri KV Burhanpur", sty_sig_sub), "",
         Paragraph("PM Shri KV Burhanpur", sty_sig_sub)],
    ]
    sig_tbl = Table(sig_data, colWidths=[6.5*cm, 4*cm, 6.5*cm])
    sig_tbl.setStyle(TableStyle([
        ("ALIGN",     (0,0), (-1,-1), "CENTER"),
        ("VALIGN",    (0,0), (-1,-1), "BOTTOM"),
        ("TOPPADDING",(0,0), (-1,-1), 3),
    ]))
    elems.append(sig_tbl)

    doc.build(elems)
    buf.seek(0)
    return buf.getvalue()


